class subdataset(Dataset):
    def  __init__(self, trainset, dataset, nosamples):
        self.dataset = dataset
        self.trainset = trainset
        self.nosamples = nosamples
        self.num_data = len(trainset)
        self.loader = DataLoader(self.trainset, shuffle=False, batch_size=self.num_data, num_workers=4)
        if dataset == 'cifar10':
            self.noperclass = int(self.nosamples/10)
            noclass = 10
        else:
            self.noperclass = int(self.nosamples/100)
            noclass = 100
        x_tmp = []
        y_tmp = []
        for i,(x,y) in enumerate(self.loader):
            for label in range(noclass):
                x1 = x[y==label]
                y1 = y[y==label]
                x1 = x1[:self.noperclass]
                y1 = y1[:self.noperclass]
                x_tmp.append(x1)
                y_tmp.append(y1)
                
        self.x_data = torch.cat(x_tmp)
        self.y_data = torch.cat(y_tmp)
        
        self.x_tmp = x_tmp
        self.y_tmp = y_tmp
        
    def __len__(self):
        return len(self.x_data)
 
    def __getitem__(self, idx):
        data = self.x_data[idx]
        targets = self.y_data[idx]
        return data, targets
        
def subcifar10(root, nosamples):
    train_set = torchvision.datasets.CIFAR10(root=root, train=True, download=True)
    test_set = torchvision.datasets.CIFAR10(root=root, train=False, download=True)
    
    subtrainset = subdataset(trainset = train_set, dataset = 'cifar10', nosamples = nosamples)
    
    return {
        'train': {'data': subtrainset.x_tmp, 'labels': subtrainset.y_tmp},
        'test': {'data': test_set.data, 'labels': test_set.targets}
    }
    
